import React, { useEffect, useCallback, useMemo, useState } from 'react';

import { nanoid } from 'nanoid';
import {
  Layout,
  Typography,
  Input,
  Form,
  Divider,
  Button,
  Card,
  Spin,
  Tag,
  Tooltip,
  Toast,
} from '@douyinfe/semi-ui';
import { IconSend, IconRefresh, IconEyeOpened, IconSync } from '@douyinfe/semi-icons';

import { EntityPropertyTypeSelector } from '../../ext/type-selector-ext';
import { EndpointHealthStatus } from '../../ext/endpoint-health-status';
import { useRemoteApiRequestStore } from '../../../stores/remote-api-request';
import { useCurrentExpression, useCurrentExpressionActions } from '../../../stores/current-api';
import { useExpressionStore } from '../../../stores/api-list';
import { useRouter } from '../../../hooks/use-router';
import { ApiUrlToolbar } from './api-url-toolbar';
import { ApiTabs } from './api-tabs';

const { Content } = Layout;
const { Text } = Typography;

interface ApiDetailPanelProps {
  selectedExpressionId?: string;
}

export const ApiDetailPanel: React.FC<ApiDetailPanelProps> = ({ selectedExpressionId }) => {
  const expressionStore = useExpressionStore();
  const currentExpression = useCurrentExpression();
  const currentExpressionActions = useCurrentExpressionActions();
  const { routeState } = useRouter();

  // ËøúÁ®ãAPIËØ∑Ê±Çstore
  const remoteApiRequest = useRemoteApiRequestStore();

  // Êú¨Âú∞Áä∂ÊÄÅ
  const [requestParameters, setRequestParameters] = useState<Record<string, any>>({});

  // üéØ ÂàõÂª∫Êñ∞APIÁöÑÈªòËÆ§Êï∞ÊçÆ
  const newApiTemplate = useMemo(() => {
    const isLocalMode = routeState.route === 'exp-local';

    if (!isLocalMode) {
      // ËøúÁ®ãAPIÊ®°Âºè
      return {
        _indexId: nanoid(),
        id: '',
        name: '',
        desc: '',
        deprecated: false,
        method: 'GET' as const,
        url: '',
        body: null,
        group: 'remote/user',
        type: 'expression' as const,
        _status: 'new' as const,
        output: {
          _indexId: nanoid(),
          id: 'result',
          type: 'u',
          name: 'ËøîÂõûÁªìÊûú',
          desc: 'APIË∞ÉÁî®ËøîÂõûÁöÑÁªìÊûú',
          required: false,
          _status: 'new' as const,
        },
        inputs: [],
      };
    } else {
      // Êú¨Âú∞ÂáΩÊï∞Ê®°Âºè
      return {
        _indexId: nanoid(),
        id: '',
        name: '',
        desc: '',
        deprecated: false,
        type: 'behavior' as const,
        _status: 'new' as const,
        output: {
          _indexId: nanoid(),
          id: 'result',
          type: 'u',
          name: 'ËøîÂõûÁªìÊûú',
          desc: 'ÂáΩÊï∞ËøîÂõûÁöÑÁªìÊûú',
          required: false,
          _status: 'new' as const,
        },
        inputs: [],
      };
    }
  }, [routeState.route]);

  // üéØ ÂèÇËÄÉÂÆû‰ΩìÊ®°ÂºèÔºöÂΩìÈÄâÊã©APIÂèòÂåñÊó∂ÔºåÊõ¥Êñ∞ÂΩìÂâçÁºñËæëÁä∂ÊÄÅ
  useEffect(() => {
    if (selectedExpressionId) {
      console.log('üîç [ApiDetailPanel] ÈÄâÊã©APIÂèòÂåñ:', selectedExpressionId);

      // Â§ÑÁêÜÊñ∞Âª∫Ê®°Âºè
      if (selectedExpressionId === 'new') {
        console.log('üîç [ApiDetailPanel] ËøõÂÖ•Êñ∞Âª∫Ê®°Âºè');
        currentExpressionActions.selectExpression(newApiTemplate);
        return;
      }

      // ‰ªéÂéüÂßãÊï∞ÊçÆ‰∏≠Ëé∑ÂèñAPI‰ø°ÊÅØ
      const originalApi = expressionStore.allItems.find((item) => item.id === selectedExpressionId);
      if (originalApi) {
        console.log('üîç [ApiDetailPanel] ËÆæÁΩÆÂΩìÂâçÁºñËæëË°®ËææÂºè:', selectedExpressionId);

        // Á°Æ‰øùÂèÇÊï∞ÂíåËæìÂá∫ÈÉΩÊúâ _indexId
        const processedApi = {
          ...originalApi,
          inputs: (originalApi.inputs || []).map((input) => ({
            ...input,
            _indexId: input._indexId || nanoid(),
          })),
          output: originalApi.output
            ? {
                ...originalApi.output,
                _indexId: originalApi.output._indexId || nanoid(),
              }
            : {
                // Êèê‰æõÈªòËÆ§ÁöÑoutputÔºåÁ°Æ‰øùÁ¨¶ÂêàBaseAttributeÁ±ªÂûã
                _indexId: nanoid(),
                id: 'result',
                name: 'ËøîÂõûÁªìÊûú',
                type: 'u',
                desc: 'APIË∞ÉÁî®ËøîÂõûÁöÑÁªìÊûú',
                required: false,
                _status: 'saved' as const,
              },
        };

        currentExpressionActions.selectExpression(processedApi);
      }
    } else {
      // Ê∏ÖÁ©∫ÈÄâÊã©
      currentExpressionActions.selectExpression(null);
    }
  }, [selectedExpressionId, expressionStore.allItems, currentExpressionActions, newApiTemplate]);

  // üéØ Ëé∑ÂèñÂΩìÂâçÁºñËæëÁöÑË°®ËææÂºè - ÁÆÄÂçïÁõ¥Êé•
  const editingApi = currentExpression.editingExpression;

  console.log('üîç [ApiDetailPanel] ÂΩìÂâçÁºñËæëÁä∂ÊÄÅ:', {
    selectedExpressionId,
    hasEditingApi: !!editingApi,
    isDirty: currentExpression.isDirty,
    editingApi: editingApi, // ÂÆåÊï¥ÁöÑÁºñËæëAPIÂØπË±°
    inputsCount: editingApi?.inputs?.length || 0,
  });

  // üéØ Â≠óÊÆµÊõ¥Êñ∞Â§ÑÁêÜ - Áõ¥Êé•Ë∞ÉÁî®storeÊñπÊ≥ï
  const handleFieldChange = useCallback(
    (field: string, value: any) => {
      console.log('üîç [ApiDetailPanel] Êõ¥Êñ∞Â≠óÊÆµ:', field, value);
      currentExpressionActions.updateProperty(field, value);
    },
    [currentExpressionActions]
  );

  // üéØ ÂèÇÊï∞Êõ¥Êñ∞Â§ÑÁêÜ - Áõ¥Êé•Ë∞ÉÁî®storeÊñπÊ≥ï
  const handleParameterChange = useCallback(
    (parameterIndexId: string, field: string, value: any) => {
      console.log('üîç [ApiDetailPanel] Êõ¥Êñ∞ÂèÇÊï∞:', parameterIndexId, field, value);
      currentExpressionActions.updateParameterProperty(parameterIndexId, field, value);
    },
    [currentExpressionActions]
  );

  // üéØ ÂèÇÊï∞Ê∑ªÂä†Â§ÑÁêÜ
  const handleAddParameter = useCallback(
    (scope: 'query' | 'header' | 'path') => {
      // üîß ‰øÆÂ§çÂ§ßÂ∞èÂÜô
      console.log('üîç [ApiDetailPanel] Ê∑ªÂä†ÂèÇÊï∞:', scope);
      const newParameter = {
        _indexId: nanoid(), // Ê∑ªÂä† _indexId
        id: `param${(editingApi?.inputs?.length || 0) + 1}`, // ‚úÖ Ê∑ªÂä†idÂ≠óÊÆµ
        name: `ÂèÇÊï∞${(editingApi?.inputs?.length || 0) + 1}`, // ‚úÖ ‰∏≠ÊñáÂêçÁß∞
        type: 's', // ‚úÖ ‰ΩøÁî®ÁÆÄÂåñÁ±ªÂûã
        desc: '', // ‚úÖ ‰ΩøÁî®descÂ≠óÊÆµ
        required: false,
        value: '', // ‚úÖ ‰ΩøÁî®valueÂ≠óÊÆµ
        scope,
      };
      currentExpressionActions.addParameter(newParameter);
    },
    [currentExpressionActions, editingApi?.inputs?.length]
  );

  // üéØ ÂèÇÊï∞Âà†Èô§Â§ÑÁêÜ
  const handleDeleteParameter = useCallback(
    (parameterIndexId: string) => {
      console.log('üîç [ApiDetailPanel] Âà†Èô§ÂèÇÊï∞:', parameterIndexId);
      currentExpressionActions.removeParameter(parameterIndexId);
    },
    [currentExpressionActions]
  );

  // üéØ Êî∂ÈõÜÂèÇÊï∞ÂÄº
  const collectParameters = useCallback(() => {
    if (!editingApi?.inputs) return {};

    const params: Record<string, any> = {};
    editingApi.inputs.forEach((input) => {
      if (input.value !== undefined && input.value !== '') {
        params[input.id] = input.value;
      }
    });

    // ÂêàÂπ∂ÊâãÂä®ËÆæÁΩÆÁöÑÂèÇÊï∞
    return { ...params, ...requestParameters };
  }, [editingApi?.inputs, requestParameters]);

  // üéØ ÂèëÈÄÅAPIËØ∑Ê±Ç
  const handleSendRequest = useCallback(async () => {
    if (!editingApi?.id) {
      console.warn('Ê≤°ÊúâÈÄâÊã©API');
      return;
    }

    try {
      const parameters = collectParameters();
      console.log('üöÄ [ApiDetailPanel] ÂèëÈÄÅËØ∑Ê±Ç:', { apiId: editingApi.id, parameters });

      await remoteApiRequest.sendRequest(editingApi.id, parameters);
    } catch (error) {
      console.error('ÂèëÈÄÅËØ∑Ê±ÇÂ§±Ë¥•:', error);
    }
  }, [editingApi?.id, collectParameters, remoteApiRequest]);

  // üéØ ÊµãËØïËøûÊé•
  const handleTestConnection = useCallback(async () => {
    if (!editingApi?.id) {
      console.warn('Ê≤°ÊúâÈÄâÊã©API');
      return;
    }

    try {
      console.log('üîó [ApiDetailPanel] ÊµãËØïËøûÊé•:', editingApi.id);
      await remoteApiRequest.testConnection(editingApi.id);
    } catch (error) {
      console.error('ËøûÊé•ÊµãËØïÂ§±Ë¥•:', error);
    }
  }, [editingApi?.id, remoteApiRequest]);

  // üéØ Ëé∑Âèñendpoint‰ø°ÊÅØ
  const endpoint = useMemo(() => {
    if (!editingApi?.url) return undefined;

    try {
      const url = new URL(editingApi.url);
      const port = url.port || (url.protocol === 'https:' ? '443' : '80');
      return `${url.hostname}:${port}`;
    } catch {
      return undefined;
    }
  }, [editingApi?.url]);

  return (
    <Content style={{ display: 'flex', flexDirection: 'column', height: '100%' }}>
      {editingApi ? (
        <div
          style={{
            padding: '16px',
            display: 'flex',
            flexDirection: 'column',
            gap: '16px',
            overflow: 'auto',
          }}
        >
          <div style={{ display: 'flex', alignItems: 'center' }}>
            <Form.Label text="Êé•Âè£" width={80} align="right" />
            <Input
              value={editingApi.id || ''}
              onChange={(value) => handleFieldChange('id', value)}
              placeholder="ID"
              style={{ fontFamily: 'monospace', flex: 1, marginLeft: '12px' }}
            />
          </div>

          <div style={{ display: 'flex', alignItems: 'center' }}>
            <Form.Label text="ÂêçÁß∞" width={80} align="right" />
            <Input
              value={editingApi.name || ''}
              onChange={(value) => handleFieldChange('name', value)}
              placeholder="APIÂêçÁß∞"
              style={{ flex: 1, marginLeft: '12px' }}
            />
          </div>

          <div style={{ display: 'flex', alignItems: 'center' }}>
            <Form.Label text="URL" width={80} align="right" />
            <div style={{ flex: 1, marginLeft: '12px' }}>
              <ApiUrlToolbar
                currentEditingApi={editingApi}
                hasUnsavedChanges={currentExpression.isDirty}
                onFieldChange={handleFieldChange}
                hideActionButtons={true}
              />
            </div>
          </div>

          <div style={{ flex: 1 }}>
            <ApiTabs
              currentEditingApi={editingApi}
              onFieldChange={handleFieldChange}
              onParameterChange={handleParameterChange}
              onAddParameter={handleAddParameter}
              onDeleteParameter={handleDeleteParameter}
            />
          </div>

          <div style={{ display: 'flex', alignItems: 'center' }}>
            <Form.Label text="ËøîÂõûÁ±ªÂûã" width={80} align="right" />
            <div style={{ flex: 1, marginLeft: '12px' }}>
              <EntityPropertyTypeSelector
                value={{
                  type: editingApi.output?.type || 'u',
                  ...(editingApi.output?.enumClassId && {
                    enumClassId: editingApi.output.enumClassId,
                  }),
                }}
                onChange={(typeInfo: any) => {
                  console.log('üîç [ApiDetailPanel] ËøîÂõûÁ±ªÂûãÂèòÊõ¥:', typeInfo);
                  // ‰ΩøÁî®ÈÄöÁî®ÁöÑ updateProperty ÊñπÊ≥ïÊõ¥Êñ∞ output ÂØπË±°
                  const updatedOutput = {
                    ...editingApi.output,
                    type: typeInfo.type,
                    ...(typeInfo.enumClassId
                      ? { enumClassId: typeInfo.enumClassId }
                      : { enumClassId: undefined }),
                  };
                  handleFieldChange('output', updatedOutput);
                }}
                disabled={false}
              />
            </div>
          </div>

          <Divider margin="16px" />

          {/* ËØ∑Ê±ÇÂèëÈÄÅÂå∫Âüü */}
          <div style={{ display: 'flex', alignItems: 'center', gap: '12px' }}>
            <Form.Label text="APIÊµãËØï" width={80} align="right" />
            <div style={{ flex: 1, display: 'flex', alignItems: 'center', gap: '12px' }}>
              <Button
                icon={<IconSend />}
                onClick={handleSendRequest}
                loading={remoteApiRequest.loading}
                disabled={!editingApi.url}
                type="primary"
              >
                ÂèëÈÄÅËØ∑Ê±Ç
              </Button>

              <Button
                icon={<IconRefresh />}
                onClick={handleTestConnection}
                loading={remoteApiRequest.loading}
                disabled={!editingApi.url}
              >
                ÊµãËØïËøûÊé•
              </Button>

              {/* Á´ØÁÇπÂÅ•Â∫∑Áä∂ÂÜµ */}
              <EndpointHealthStatus endpoint={endpoint} mode="full" showRefresh={true} />
            </div>
          </div>

          {/* ÂìçÂ∫îÁªìÊûúÂ±ïÁ§∫ */}
          <div style={{ padding: '16px', borderBottom: '1px solid var(--semi-color-border)' }}>
            <div style={{ display: 'flex', alignItems: 'flex-start' }}>
              <Form.Label text="ÂìçÂ∫îÁªìÊûú" width={80} align="right" />
              <div style={{ flex: 1, marginLeft: '12px' }}>
                {remoteApiRequest.currentResponse ? (
                  <div>
                    <div style={{ marginBottom: '8px', fontSize: '12px', color: '#666' }}>
                      Áä∂ÊÄÅ: {remoteApiRequest.currentResponse.statusCode}{' '}
                      {remoteApiRequest.currentResponse.statusText} | ËÄóÊó∂:{' '}
                      {remoteApiRequest.currentResponse.responseTimeMs}ms | Êó∂Èó¥:{' '}
                      {new Date(remoteApiRequest.currentResponse.requestTime).toLocaleString()}
                    </div>
                    <pre
                      style={{
                        background: '#f6f8fa',
                        padding: '12px',
                        borderRadius: '4px',
                        fontSize: '12px',
                        maxHeight: '300px',
                        overflow: 'auto',
                        whiteSpace: 'pre-wrap',
                        wordBreak: 'break-word',
                      }}
                    >
                      {JSON.stringify(remoteApiRequest.currentResponse.body, null, 2)}
                    </pre>
                  </div>
                ) : (
                  <div style={{ color: '#999', fontStyle: 'italic' }}>ÁÇπÂáªÂèëÈÄÅËØ∑Ê±ÇÊåâÈíÆÊµãËØïAPI</div>
                )}
              </div>
            </div>
          </div>

          {/* ÈîôËØØ‰ø°ÊÅØÊòæÁ§∫ */}
          {remoteApiRequest.error && (
            <Card title="ÈîôËØØ‰ø°ÊÅØ" style={{ marginTop: '16px', borderColor: '#ff4d4f' }}>
              <Text type="danger">{remoteApiRequest.error}</Text>
            </Card>
          )}
        </div>
      ) : (
        <div
          style={{
            display: 'flex',
            alignItems: 'center',
            justifyContent: 'center',
            height: '100%',
          }}
        >
          <Text type="secondary">ËØ∑ÈÄâÊã©Â∑¶‰æßAPIÊü•ÁúãËØ¶ÊÉÖ</Text>
        </div>
      )}
    </Content>
  );
};
